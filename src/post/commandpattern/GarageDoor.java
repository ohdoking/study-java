package post.commandpattern;

public class GarageDoor {

	public void up(){
		System.out.println("창고문 열기");
	}
	public void down(){
		System.out.println("창고문 닫기");
	}
	public void stop(){
		System.out.println("창고문 멈춰");
	}
	public void lightOn(){
		System.out.println("창고 불 켜기 ");
	}
	public void lightOff(){
		System.out.println("창고 불 끄기");
	}
}
